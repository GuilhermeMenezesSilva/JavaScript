// push (adiciona no fim) e unshift (adiciona no inicio) 
let pessoas = ["Rose", "Sergio", "Vinicius", "Guilherme", "Matheus"]
console.log("Lista da familia: ", pessoas)

console.log()

pessoas.push("Elaine")
console.log("Lista da familia depois de 2019: ", pessoas)

console.log()

pessoas.unshift('Spyke');
console.log("Lista da familia depois de 2022: ", pessoas)


